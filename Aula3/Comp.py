#== (igual)
#!= (diferente)
#> (maior que)
#< (menor que)
#>= (maior ou igual)
#<= (menor ou igual)

a,b = 11, 20
if(a==b):
    print("a e igual")

if (a!=b):
    print("A nao e igual a b")

if( a > b):
    print("A e maior que b")

if(a < b):
    print("A e menor que b")

if( a >= b):
    print("A e maior ou igual a b" )

if( a <= b):
    print("A e menor ou igual a b")
